"""Repository ports — one protocol per aggregate. Implementations live in
app/infrastructure/repositories. Method sets are intentionally minimal;
they grow only when a use case needs a new query."""
from datetime import date, datetime
from typing import Protocol
from uuid import UUID

from app.domain.alerting.alert import Alert
from app.domain.alerting.notification import Notification
from app.domain.identity.user import User
from app.domain.intelligence.fundamentals import FundamentalSnapshot
from app.domain.intelligence.news import NewsArticle
from app.domain.intelligence.technicals import TechnicalSnapshot
from app.domain.learning.evaluation import LearningRecord, PredictionEvaluation
from app.domain.market.company import Company
from app.domain.market.market_event import MarketEvent
from app.domain.market.price import PriceBar, PriceInterval
from app.domain.portfolio.portfolio import Portfolio
from app.domain.portfolio.watchlist import Watchlist
from app.domain.research.prediction import Prediction
from app.domain.research.reasoning import AIReasoning
from app.domain.research.recommendation import Recommendation
from app.domain.research.report import ResearchReport


class UserRepository(Protocol):
    async def get(self, user_id: UUID) -> User | None: ...
    async def get_by_email(self, email: str) -> User | None: ...
    async def add(self, user: User) -> None: ...
    async def update(self, user: User) -> None: ...
    async def count_all(self) -> int: ...


class CompanyRepository(Protocol):
    async def get(self, company_id: UUID) -> Company | None: ...
    async def get_by_symbol(self, symbol: str) -> Company | None: ...
    async def search(self, query: str, sector: str | None, page: int, size: int) -> tuple[list[Company], int]: ...
    async def add(self, company: Company) -> None: ...
    async def update(self, company: Company) -> None: ...
    async def list_active_symbols(self) -> list[str]: ...


class PriceRepository(Protocol):
    async def add_bars(self, bars: list[PriceBar]) -> int: ...
    async def get_bars(
        self, company_id: UUID, interval: PriceInterval, start: datetime, end: datetime
    ) -> list[PriceBar]: ...
    async def latest_bar(self, company_id: UUID, interval: PriceInterval) -> PriceBar | None: ...


class NewsRepository(Protocol):
    async def add(self, article: NewsArticle) -> None: ...
    async def exists_by_url(self, url: str) -> bool: ...
    async def get_unanalyzed(self, limit: int) -> list[NewsArticle]: ...
    async def update(self, article: NewsArticle) -> None: ...
    async def for_company(self, company_id: UUID, page: int, size: int) -> tuple[list[NewsArticle], int]: ...
    async def trending(self, limit: int) -> list[NewsArticle]:
        """Most important recently-analyzed articles across all companies —
        backs the dashboard's global news feed."""
        ...


class TechnicalsRepository(Protocol):
    async def save_snapshot(self, snapshot: TechnicalSnapshot) -> None: ...
    async def latest(self, company_id: UUID, interval: PriceInterval) -> TechnicalSnapshot | None: ...


class FundamentalsRepository(Protocol):
    async def save(self, snapshot: FundamentalSnapshot) -> None: ...
    async def latest(self, company_id: UUID) -> FundamentalSnapshot | None: ...
    async def history(self, company_id: UUID, period: str, limit: int) -> list[FundamentalSnapshot]: ...


class MarketEventRepository(Protocol):
    async def add(self, event: MarketEvent) -> None: ...
    async def between(self, start: date, end: date) -> list[MarketEvent]: ...


class RecommendationRepository(Protocol):
    async def add(self, rec: Recommendation) -> None: ...
    async def get(self, recommendation_id: UUID) -> Recommendation | None: ...
    async def active_for_company(self, company_id: UUID) -> Recommendation | None: ...
    async def update(self, rec: Recommendation) -> None: ...
    async def screen(
        self, min_score: float, sector: str | None, page: int, size: int
    ) -> tuple[list[Recommendation], int]: ...


class ResearchReportRepository(Protocol):
    async def add(self, report: ResearchReport) -> None: ...
    async def latest_for_company(self, company_id: UUID) -> ResearchReport | None: ...


class AIReasoningRepository(Protocol):
    async def add(self, reasoning: AIReasoning) -> None: ...
    async def get(self, reasoning_id: UUID) -> AIReasoning | None: ...


class PredictionRepository(Protocol):
    async def add(self, prediction: Prediction) -> None: ...
    async def due_for_evaluation(self, now: datetime, limit: int) -> list[Prediction]: ...
    async def for_company(self, company_id: UUID, limit: int) -> list[Prediction]: ...


class LearningRepository(Protocol):
    async def add_evaluation(self, evaluation: PredictionEvaluation) -> None: ...
    async def save_record(self, record: LearningRecord) -> None: ...
    async def get_record(self, scope: str, key: str, window: str) -> LearningRecord | None: ...
    async def list_by_scope(self, scope: str) -> list[LearningRecord]: ...


class PortfolioRepository(Protocol):
    async def get(self, portfolio_id: UUID) -> Portfolio | None: ...
    async def for_user(self, user_id: UUID) -> list[Portfolio]: ...
    async def add(self, portfolio: Portfolio) -> None: ...
    async def update(self, portfolio: Portfolio) -> None: ...
    async def delete(self, portfolio_id: UUID) -> None: ...


class WatchlistRepository(Protocol):
    async def get(self, watchlist_id: UUID) -> Watchlist | None: ...
    async def for_user(self, user_id: UUID) -> list[Watchlist]: ...
    async def add(self, watchlist: Watchlist) -> None: ...
    async def update(self, watchlist: Watchlist) -> None: ...
    async def delete(self, watchlist_id: UUID) -> None: ...
    async def all_watched_company_ids(self) -> set[UUID]: ...


class AlertRepository(Protocol):
    async def get(self, alert_id: UUID) -> Alert | None: ...
    async def for_user(self, user_id: UUID) -> list[Alert]: ...
    async def active_for_company(self, company_id: UUID) -> list[Alert]: ...
    async def add(self, alert: Alert) -> None: ...
    async def update(self, alert: Alert) -> None: ...
    async def delete(self, alert_id: UUID) -> None: ...
    async def count_active(self) -> int: ...


class NotificationRepository(Protocol):
    async def add(self, notification: Notification) -> None: ...
    async def for_user(
        self, user_id: UUID, unread_only: bool, page: int, size: int
    ) -> tuple[list[Notification], int]: ...
    async def mark_all_read(self, user_id: UUID, at: datetime) -> int: ...
